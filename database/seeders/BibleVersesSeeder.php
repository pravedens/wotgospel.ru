        // Используем публичный API bibleapi.co (синодальный перевод)
        $books = $this->getBooksList();

        $totalVerses = 0;

        foreach ($books as $bookId => $bookName) {
            $this->command->info("Обработка книги: {$bookName}");

            $chapters = $this->getChaptersCount($bookId);

            for ($chapter = 1; $chapter <= $chapters; $chapter++) {
                $verses = $this->getChapterVerses($bookId, $chapter);

                foreach ($verses as $verseNumber => $verseText) {
                    DB::table('bible_verses')->insert([
                        'book' => $bookName,
                        'book_abbr' => $this->getBookAbbreviation($bookName),
                        'chapter' => $chapter,
                        'verse' => $verseNumber,
                        'text' => $verseText,
                        'created_at' => now(),
                        'updated_at' => now(),
                    ]);
                    $totalVerses++;
                }
            }
        }

        $this->command->info("Импорт завершён! Добавлено стихов: {$totalVerses}");
    }

