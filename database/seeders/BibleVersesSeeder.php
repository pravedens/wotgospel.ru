    private function getChapterVerses(int $bookId, int $chapter): array
    {
        // API для получения текста главы
        $url = "https://bibleapi.co/api/rst/{$bookId}/{$chapter}";

        try {
            $response = Http::timeout(30)->get($url);

            if ($response->successful()) {
                $data = $response->json();
                if (isset($data['versos'])) {
                    $verses = [];
                    foreach ($data['versos'] as $verse) {
                        $verses[(int)$verse['numero']] = $verse['texto'];
                    }
                    return $verses;
                }
            }
        } catch (\Exception $e) {
            $this->command->warn("Ошибка загрузки книги {$bookId}, главы {$chapter}: " . $e->getMessage());
        }

        return [];
    }

